def flag(val, low=None, high=None, higher_is_better=False):
    if val is None:
        return "-", False
    if higher_is_better:
        return f"{val:.1f}", val < low
    if (low is not None and val < low) or (high is not None and val > high):
        return f"{val:.1f}", True
    return f"{val:.1f}", False


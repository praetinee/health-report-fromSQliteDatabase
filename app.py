        st.write("‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏î‡∏π‡∏ú‡∏•‡∏ï‡∏£‡∏ß‡∏à‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì")

        with st.form("web_login_form"):
            input_name = st.text_input("‡∏ä‡∏∑‡πà‡∏≠")
            input_surname = st.text_input("‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏•")
            input_id_card = st.text_input("‡πÄ‡∏•‡∏Ç‡∏ö‡∏±‡∏ï‡∏£‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏ä‡∏ô (13 ‡∏´‡∏•‡∏±‡∏Å)")
            
            submitted = st.form_submit_button("‡∏î‡∏π‡∏ú‡∏•‡∏ï‡∏£‡∏ß‡∏à")


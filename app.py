                    conn = get_connection()
                    query = "SELECT * FROM health_data WHERE name = ? AND surname = ? AND id_card = ?"
                    # ‡πÉ‡∏ä‡πâ strip() ‡∏ï‡∏±‡∏î‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ä‡∏±‡∏ß‡∏£‡πå
